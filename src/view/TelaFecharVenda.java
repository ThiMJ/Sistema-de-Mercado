    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        venda.setData(dataCompleta);

        verificarMetodoPagamento();
        verificarCliente();

        vendaAcao.salvarVenda(venda);
        itemAcao.salvarItens(itens);
        prodAcao.baixarEstoqueVenda(itens);

        //chamando os métodos da TelaVenda através da própria instancia da tela recebida no método transferirDados
        telaVenda.limparCampos();
        telaVenda.itemAcao.excluirTodosItens();
        telaVenda.idVenda();

        this.dispose();

    }//GEN-LAST:event_btnFinalizarActionPerformed

