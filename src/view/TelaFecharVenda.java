    public void listarMetodosPag() {
        pags = pagAcao.listarPags();

        for (int i = 0; i < pags.size(); i++) {
            comboMetodoPag.addItem(pags.get(i).getFormaPag());
        }
    }

    public void listarClientes() {
        clientes = cliAcao.listarClientes();

        for (int i = 0; i < clientes.size(); i++) {
            comboCliente.addItem(clientes.get(i).getNome());
        }
    }

    public void data() {
        Date data = new Date();

        SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd");

        dataCompleta = df.format(data).replaceAll("/", "");
    }

    //transferir dados de uma tela para outra sem ser pelo construtor
    public void transferirDados(Vendas venda, ArrayList<ItensVenda> itens, TelaVenda telaVenda) {
        this.venda = venda;
        this.telaVenda = telaVenda;

        for (int i = 0; i < itens.size(); i++) {
            ItensVenda item = new ItensVenda();

            item.setIdProd(itens.get(i).getIdProd());
            item.setIdVenda(itens.get(i).getIdVenda());
            item.setQtd(itens.get(i).getQtd());
            item.setVlrTotal(itens.get(i).getVlrTotal());
            item.setVlrUn(itens.get(i).getVlrUn());

            this.itens.add(item);
        }

        fieldVlrTotal.setText(String.valueOf(venda.getValorTotal()));
    }

    public void verificarMetodoPagamento() { //verifica qual o método de pagamento escolhido
        String metodoPag = comboMetodoPag.getSelectedItem().toString();

        //verifica qual método de pagamento foi o escolhido e seta com o id do respectivo metodo
        for (int i = 0; i < pags.size(); i++) {
            if (pags.get(i).getFormaPag().equalsIgnoreCase(metodoPag)) {
                venda.setIdPag(pags.get(i).getIdPag());
            }
        }
    }

    public void verificarCliente() { //verifica qual o cliente escolhido
        String cliente = comboCliente.getSelectedItem().toString();

        //verifica qual o cliente escolhido e seta com o id do respectivo cliente
        for (int i = 0; i < clientes.size(); i++) {
            if (clientes.get(i).getNome().equalsIgnoreCase(cliente)) {
                venda.setIdCli(clientes.get(i).getId());
            }
        }

    }

