    private void btnGravarCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarCliActionPerformed

        if (!fieldIdCli.getText().isEmpty()) {

            int id = Integer.parseInt(fieldIdCli.getText());

            boolean achou = cliAcao.buscarId(id); //verificando se o cliente já tem cadastro ou não

            cli.setCpf(fieldCpfCli.getText());
            cli.setDataNasc(fieldDataNascCli.getText().replaceAll("/", ""));
            cli.setEmail(fieldEmailCli.getText());
            cli.setId(Integer.parseInt(fieldIdCli.getText()));
            cli.setNome(fieldNomeCli.getText());
            cli.setRg(fieldRgCli.getText());

            end.setBairro(fieldBairroCli.getText());
            end.setCep(fieldCepCli.getText());
            end.setCidade(fieldCidCli.getText());
            end.setComplemento(fieldCompCli.getText());
            end.setLogradouro(fieldEndCli.getText());
            end.setNum(Integer.parseInt(fieldnumCli.getText()));

            if (comboStatusCli.getSelectedIndex() == 0) {
                cli.setStatus(1);

            } else {
                cli.setStatus(0);
            }

            if (achou) { //Id já está em uso portanto vai atualizar o cliente. Então não precisa alterar o id de endereço
                cliAcao.atualizarCli(cli);
                endAcao.atualizarEnd(end);

            } else { //id ainda não está em uso portanto vai adicionar o cliente

                int idMaxEnd = endAcao.buscarIdMax();

                cli.setIdEndereco(idMaxEnd + 1);
                end.setIdEndereco(cli.getIdEndereco());

                endAcao.cadastrarEnd(end);
                cliAcao.cadastrarCli(cli);

            }

        } else {
            JOptionPane.showMessageDialog(null, "Para Gravar deve-se ter campos válidos");
        }

        limparCamposCli();
        desabilitarCamposCli();
    }//GEN-LAST:event_btnGravarCliActionPerformed

