    private void btnGravarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarFuncActionPerformed
        boolean senhasIguais = false;

        if (!fieldIdFunc.getText().isEmpty()) {

            int id = Integer.parseInt(fieldIdFunc.getText());

            boolean achou = funcAcao.buscarId(id); //verificando se o funcionário já tem cadastro ou não

            func.setAdmissao(fieldDataAdmFunc.getText().replaceAll("/", ""));
            func.setCpf(fieldCpfFunc.getText());
            func.setDataNasc(fieldDataNascFunc.getText().replaceAll("/", ""));
            func.setId(Integer.parseInt(fieldIdFunc.getText()));
            func.setLogin(fieldLoginFunc.getText());
            func.setNome(fieldNomeFunc.getText());
            func.setRg(fieldRgFunc.getText());
            func.setSalario(Double.parseDouble(fieldSalFunc.getText().replace(",", ".")));

            //não há problema em setar a senha pois no método de atualizar não atualizar a senha. Há um método a parte para isso.
            if (fieldSenha1Func.getText().equals(fieldSenha2Func.getText())) { // verificando se as senhas são iguais
                func.setSenha(cript.criptografar(fieldSenha1Func.getText())); //criptografa a senha
                senhasIguais = true;
            }

            //verificando o cargo
            if (comboCargoFunc.getSelectedIndex() == 0) {
                func.setIdCargo(3); //3 = Antendente referente ao banco

            } else {
                func.setIdCargo(1); //1 = Gerente referente ao banco
            }

            end.setBairro(fieldBairroFunc.getText());
            end.setCep(fieldCepFunc.getText());
            end.setCidade(fieldCidFunc.getText());
            end.setComplemento(fieldCompFunc.getText());
            end.setLogradouro(fieldEndFunc.getText());
            end.setNum(Integer.parseInt(fieldnumFunc.getText()));

            if (senhasIguais) { //senhas iguais, deixa atualizar ou cadastrar. Caso contrário não deixa

                if (achou) { //Id já está em uso portanto vai atualizar o funcionário. Então não precisa alterar o id de endereço

                    funcAcao.atualizarFunc(func); //não atualiza a senha por padrão
                    endAcao.atualizarEnd(end);

                    if (alterarSenha == 0) { //deseja alterar também a senha - 0 é sim para a opção escolhida
                        funcAcao.atualizarSenha(func.getId(), func.getSenha()); //atualiza a senha
                    }

                } else { //id ainda não está em uso portanto vai adicionar o funcionario

                    int idMaxEnd = endAcao.buscarIdMax();

                    func.setIdEndereco(idMaxEnd + 1);
                    end.setIdEndereco(func.getIdEndereco());

                    endAcao.cadastrarEnd(end);
                    funcAcao.cadastrarFunc(func);

                }
            } else {
                JOptionPane.showMessageDialog(null, "Senhas Incompatíveis");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Para Gravar deve-se ter campos válidos");
        }

        limparCamposFunc();
        desabilitarCamposFunc();
    }//GEN-LAST:event_btnGravarFuncActionPerformed

