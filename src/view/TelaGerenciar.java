    public void listarVendasTabela(ArrayList<Vendas> vendas) {
        tableRelVen.setNumRows(0);

        Funcionarios funcionario;
        Pagamentos pagamento;
        Clientes cliente;

        for (int i = 0; i < vendas.size(); i++) {
            //pegando as chaves estrangeiras da venda e buscando os seus respectivos valores nas outras tabelas
            funcionario = funcAcao.pesquisarFunc(vendas.get(i).getIdFunc());
            pagamento = pagAcao.pesquisarPag(vendas.get(i).getIdPag());
            cliente = cliAcao.pesquisarCli(vendas.get(i).getIdCli());

            tableRelVen.addRow(new Object[]{
                vendas.get(i).getId(),
                vendas.get(i).getData(),
                vendas.get(i).getValorTotal(),
                funcionario.getNome(),
                pagamento.getFormaPag(),
                cliente.getNome()
            });
        }
    }

