    private void tableItensMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableItensMouseClicked

        if (evt.getClickCount() == 2) { //verificando se o total de cliques é igual a 2
            int op = JOptionPane.showConfirmDialog(null, "Deseja excluir o Produto selecionado?");

            if (op == 0) { // 0 == sim
                int ind = tableItens.getSelectedRow();

                itemAcao.excluirItem(ind); //excluindo item dos arrays

                listarItensTabela();
                atualizarCampoTotal();
                atualizarCampoTotalItem();
            }
        }

        fieldProd.requestFocus();

    }//GEN-LAST:event_tableItensMouseClicked

    public void listarItensTabela() { //listando os itens na tabela
        table.setNumRows(0);

        prods = itemAcao.produtosItens(); //buscando os produtos adicionados como item
        itens = itemAcao.itens(); //buscando os itens adicionados

        qtdItens = 0;

        for (int i = 0; i < prods.size(); i++) {
            qtdItens++;

            table.addRow(new Object[]{
                qtdItens,
                prods.get(i).getDescProd(),
                prods.get(i).getVlrUnVenda(),
                itens.get(i).getQtd(),
                itens.get(i).getVlrTotal()
            });
        }
    }

    public void idVenda() {
        venda.setId(vendaAcao.buscarIdMax() + 1);
    }

    public void atualizarCampoTotalItem() {
        labelQtdItens.setText(String.valueOf(qtdItens));
    }

    public void atualizarCampoTotal() {
        labelTotal.setText(String.valueOf(itemAcao.valorTotalItens()));
    }

    public void limparCampos() {
        fieldProd.setText(null);
        labelDescProd.setText("CAIXA DISPONÍVEL");
        labelVlUn.setText(null);
        labelQtd.setText(null);
        labelVlParcial.setText(null);
        labelQtdItens.setText(null);
        labelTotal.setText(null);
        table.setNumRows(0);
    }

    public void escreverCampoCodProd(String cod) { //escreve o código do produto no field Prod quando foi pesquisado 
        //um produto pela tecla F10
        fieldProd.setText(fieldProd.getText() + cod);
    }

    public void transferirDados(Funcionarios func) { //usado para transferir dados emtre as telas sem precisar do construtor
        this.func = func;

        labelAtend.setText(this.func.getNome());

        venda.setIdFunc(this.func.getId());
    }

