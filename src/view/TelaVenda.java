    private void fieldProdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldProdKeyPressed
        //verificando as teclas pressionadas
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) { //adiciona o item
            try {
                int pos = fieldProd.getText().indexOf("*"); //pegando a posição do caractere *

                qtd = Integer.parseInt(fieldProd.getText().substring(0, pos)); //pegando os caracteres até o *
                codProd = fieldProd.getText().substring((pos + 1)); //pegando os caracteres a partir do *

                prod = prodAcao.pesquisarCodProd(codProd); //pesquisando produto pelo código

                if (prod.getIdProd() != 0) {
                    //df.format está convertendo para duas casas decimais
                    vlrParcial = Double.parseDouble(df.format(qtd * prod.getVlrUnVenda()).replace(",", "."));

                    //adicionando item no array list
                    itemAcao.adicionarItem(prod, qtd, venda.getId());

                    atualizarCampoTotal();

                    //listando o item na tabela
                    listarItensTabela();

                    //atualizando os campos dos labels
                    labelVlUn.setText(String.valueOf(prod.getVlrUnVenda()));
                    labelQtd.setText(String.valueOf(qtd));
                    labelVlParcial.setText(String.valueOf(vlrParcial));
                    labelDescProd.setText(prod.getDescProd().toUpperCase());
                    atualizarCampoTotalItem();
                    atualizarCampoTotal();

                    //rolando o scroll para baixo automaticamente
                    int max = jScrollPane2.getVerticalScrollBar().getMaximum(); //pega o ponto máximo do scrollBar
                    jScrollPane2.getViewport().setViewPosition(new Point(0, max)); //seta o ponto onde o ScrollBar deve estar

                    //limpando o field de entrada
                    fieldProd.setText(null);

                } else {
                    JOptionPane.showMessageDialog(null, "Produto Não Encontrado");
                }

            } catch (Exception ex) {
                System.out.println("Erro de inserção de produto: " + ex);
                JOptionPane.showMessageDialog(null, "O formato de inserção deve ser qtd*código do produto");
            }
        }

        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_F2) { //fecha a venda
            TelaFecharVenda tela = new TelaFecharVenda();

            venda.setValorTotal(itemAcao.valorTotalItens());

            //o this passa a própria instância dessa tela, para que a outra tela possa acessar os métodos e atributos
            //sem criar ouytra instância, para que os efeitos sejam feitos nessa mesma tela
            tela.transferirDados(venda, itens, this);
            tela.setVisible(true);
        }

        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_F3) { //cancela a venda
            int op = JOptionPane.showConfirmDialog(null, "Deseja cancelar a Venda?");

            if (op == 0) { //0 == sim

                itemAcao.excluirTodosItens();
                limparCampos();
                idVenda();
            }
        }

        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_F9) { //cancela produto
            JOptionPane.showMessageDialog(null, "Clique duas vezes no produto na tabela para cancelá-lo");
        }

        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_F10) { //procura um produto
            JOptionPane.showMessageDialog(null, "Clique duas vezes sobre o produto desejado");

            TelaListarProdutos tela = new TelaListarProdutos();

            tela.transferirDados(this); //o this está passando a instância dessa tela
            tela.setVisible(true);
        }

        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_F11) { //sair
            int op = JOptionPane.showConfirmDialog(null, "Deseja sair do Sistema?");

            if (op == 0) {
                this.dispose();
                TelaLogin tela = new TelaLogin();
                tela.setVisible(true);
            }
        }
    }//GEN-LAST:event_fieldProdKeyPressed

